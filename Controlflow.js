//loops in js
x=0;
y=0;

//while loop
console.log("~~~~~~~~~~~~~~~~~~~~~~~~~~~while loop log~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~");
while(x<20){
    console.log(x);
    x++;
}

//do-while loop
console.log("~~~~~~~~~~~~~~~~~~~~~~~~~~~do while loop log~~~~~~~~~~~~~~~~~~~~~~~~~~");
do{
    console.log(y);
    y++
}
while(y<20);

//for loop
console.log("~~~~~~~~~~~~~~~~~~~~~~~~~~~for loop log~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~");
for(i=0;i<20;i++){
    console.log(i);
}